package main;

public class Portales {

	String portal;

	public Portales(String portal) {

		this.portal = portal;
		
	}

	public String getPortal() {
		
		return portal;
		
	}

	public void setPortal(String portal) {
		
		this.portal = portal;
		
	}

	@Override
	public String toString() {
		
		return "Portales [portal=" + portal + "]";
		
	}
	
	
	
}
